package BasicJava;

class Var_DataType {
   public static void main(String[] args) {
      int i = 10 ;
      char c = 'P';
      float f = 10.32f;
      double d = 202.20;
      long l = 1514455464348L ;


      System.out.println("The Variable " + i + " is Integer");
      System.out.println("the variable " + c + " is Char");
      System.out.println("the variable " + f + " is Float");
      System.out.println("the variable " + d + " is Double");
      System.out.println("the variable " + l + " is Long");


   } 
}
