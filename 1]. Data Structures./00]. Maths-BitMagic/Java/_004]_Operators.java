package BasicJava;

class Operators {
   public static void main(String[] args) {
       int a = 100 ;
       int b = 50 ;

       System.out.println("Entered value are "+a + " " + b + " ");

       int y1 = a + b ;
       int y2 = a - b ;
       int y3 = a * b ;
       int y4 = a / b ;
       int y5 = a % b ;

       System.out.println("After Addition " + y1);
       System.out.println("After Subtraction " + y2);
       System.out.println("After Multiplication " + y3);
       System.out.println("After Division " + y4);
       System.out.println("After Modulus " + y5);

    } 
}

